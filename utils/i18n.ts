export type TranslationKey = keyof typeof translations.zh

export function getTranslation(lang: LangCode, key: TranslationKey): string {
  return translations[lang as keyof typeof translations]?.[key] || translations.zh[key] || key
}

export function useLanguage() {
  const [lang, setLang] = useState<LangCode>('en')
  
  useEffect(() => {
    // 优先读取本地保存的语言
    const saved = typeof localStorage !== 'undefined' ? localStorage.getItem('lang') : null
    let next: LangCode | undefined
    const savedMatch = LANG_OPTIONS.find(o => o.code === saved)
    if (savedMatch) {
      next = savedMatch.code as LangCode
    } else if (typeof navigator !== 'undefined') {
      // 设备语言检测（languages 优先，其次 language）
      const candidates: string[] = []
      if (Array.isArray((navigator as any).languages)) {
        candidates.push(...((navigator as any).languages as string[]))
      }
      if ((navigator as any).language) {
        candidates.push((navigator as any).language as string)
      }
      // 匹配策略：先完整匹配 html 码，再匹配主语言码
      for (const cand of candidates) {
        const lower = (cand || '').toLowerCase()
        const primary = lower.split('-')[0]
        const byHtml = LANG_OPTIONS.find(o => o.html.toLowerCase() === lower)
        if (byHtml) {
          next = byHtml.code as LangCode
          break
        }
        const byPrimary = LANG_OPTIONS.find(o => o.code === primary)
        if (byPrimary) {
          next = byPrimary.code as LangCode
          break
        }
      }
    }

    const finalLang: LangCode = next || 'en'
    setLang(finalLang)
    // 同步到本地与 Cookie，方便 SSR 使用
    try {
      localStorage.setItem('lang', finalLang)
      document.cookie = `lang=${finalLang}; path=/; max-age=31536000; samesite=lax`
    } catch {}
    // 设置 HTML 属性
    const match = LANG_OPTIONS.find(o => o.code === finalLang)
    if (match) {
      document.documentElement.lang = match.html
      document.documentElement.dir = finalLang === 'ar' || finalLang === 'he' ? 'rtl' : 'ltr'
    }
  }, [])
  
  // 监听语言变化事件
  useEffect(() => {
    const handleLanguageChange = (event: CustomEvent) => {
      const newLang = event.detail as LangCode
      setLang(newLang)
      // 设置 HTML 属性
      const match = LANG_OPTIONS.find(o => o.code === newLang)
      if (match) {
        document.documentElement.lang = match.html
        document.documentElement.dir = newLang === 'ar' || newLang === 'he' ? 'rtl' : 'ltr'
      }
    }
    
    window.addEventListener('languageChange', handleLanguageChange as EventListener)
    return () => window.removeEventListener('languageChange', handleLanguageChange as EventListener)
  }, [])
  
  const t = useCallback((key: TranslationKey) => getTranslation(lang, key), [lang])
  
  return { lang, setLang, t }
} 